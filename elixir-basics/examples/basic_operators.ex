# Arithmetic
-1 + 8 * 3 / 2 # 11.0
div(5, 2) # 2
rem(5, 2) # 1

# Lists
[2, "k"] ++ [1, 7] # [2, "k", 1, 7]
[2, "k", 1, 7] -- [1, 7] # [2, "k"]

# Boolean
true && false # false
true || false # true
!true # false

19 != 84 # true
2 * 2 == 5 # false
-0.99 > -1 # true

# Strings
parrot = "Keksha"
"Hello #{parrot}" # Hello Keksha
parrot <> " Peksha" # Keksha Peksha
